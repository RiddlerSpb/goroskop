function generateHoroscope() {
    // Список знаков Зодиака и соответствующих гороскопов
    const horoscopes = {
        'Овен': 'Ожидается удачный день для торговли криптовалютами. Будьте готовы к неожиданным изменениям!',
        'Телец': 'Сегодня отличный день для стабильных инвестиций. Рассмотрите долгосрочные стратегии.',
        'Близнецы': 'Вы будете в центре событий. Не забывайте следить за новостями рынка.',
        'Рак': 'Подходящий день для размышлений и анализа рыночной ситуации.',
        'Лев': 'Будьте активны в торговле, но не забывайте профессиональную консультацию.',
        'Дева': 'Сегодня важно поддерживать дисциплину в инвестициях. Особое внимание уделите деталям.',
        'Весы': 'Вам может понадобиться балансировать риски и потенциалы в криптовалютных операциях.',
        'Скорпион': 'День будет насыщен событиями. Подготовьтесь к различным инвестиционным возможностям.',
        'Стрелец': 'Сегодняшний день подходит для экспериментов в инвестициях. Не бойтесь пробовать новое.',
        'Козерог': 'Ожидается стабильное восстановление рынка. Рассмотрите возможности для роста капитала.',
        'Водолей': 'Будьте готовы к изменениям в рыночной конъюнктуре. Возможны неожиданные возможности.',
        'Рыбы': 'Сегодня ваш интуитивный подход может принести вам успех в инвестициях.'
    };

    // Выбираем случайный знак Зодиака для прогноза
    const zodiacSigns = Object.keys(horoscopes);
    const randomSign = zodiacSigns[Math.floor(Math.random() * zodiacSigns.length)];

    // Получаем гороскоп для выбранного знака
    const horoscopeText = horoscopes[randomSign];

    // Выводим гороскоп на страницу
    const horoscopeElement = document.getElementById('horoscopeText');
    horoscopeElement.textContent = `${randomSign}: ${horoscopeText}`;
}
